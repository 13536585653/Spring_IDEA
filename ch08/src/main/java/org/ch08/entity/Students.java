public class Students {

	private String sid;
	private String stuName;
	private int age;

public String getSid() {
	return sid;
}

public void setSid(String sid) {
	this.sid = sid;
}

public String getStuName() {
	return stuName;
}

public void setStuName(String stuName) {
	this.stuName = stuName;
}

public int getAge() {
	return age;
}

public void setAge(int age) {
	this.age = age;
}
}
